# Replacing ! with blank space
sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."
print(sentence.replace("!", " "))

# Reprinting sentence in upper cases 
sentence_rep = "The quick brown fox jumps over the lazy dog."
print(sentence_rep.upper())

# Sentence in reverse 
position = sentence_rep[::-1]
print(position)
